# ===== 1. Load required libraries =====
library(Seurat)
library(monocle3)
library(SingleCellExperiment)
library(ggplot2)
library(patchwork)  # For combining plots side-by-side

# ===== 2. Load full Seurat object and subset data =====
seurat_obj <- readRDS("/projectnb/cepinet/data/scRNA/cell-2023-Sun/ROSMAP.Microglia.6regions.seurat.harmony.selected.deidentified.rds")

# Visualize clustering of full Seurat object
p1 <- DimPlot(seurat_obj, group.by = "seurat_clusters", label = TRUE)
ggsave("1.UMAP_seurat_clusters.pdf", plot = p1, width = 6.5, height = 5)

# Load subsets by disease stage
seurat_subset_non <- readRDS("/projectnb/cepinet/users/vhe/Na_Cell_2023_MG/seurat_subset_nonAD.rds")
seurat_subset_ear <- readRDS("/projectnb/cepinet/users/vhe/Na_Cell_2023_MG/seurat_subset_earlyAD.rds")
seurat_subset_lat <- readRDS("/projectnb/cepinet/users/vhe/Na_Cell_2023_MG/seurat_subset_lateAD.rds")

# Visualize clustering of nonAD subset
p1 <- DimPlot(seurat_subset_non, group.by = "seurat_clusters", label = TRUE)
ggsave("1.UMAP_seurat_clusters_nonAD.pdf", plot = p1, width = 6.5, height = 5)

# ===== 3. Construct Monocle3 CellDataSet from nonAD Seurat object =====
counts <- GetAssayData(seurat_subset_non, assay = "RNA", slot = "counts")
cell_metadata <- seurat_subset_non@meta.data
gene_metadata <- data.frame(gene_short_name = rownames(counts), row.names = rownames(counts))

cds <- new_cell_data_set(
  expression_data = counts,
  cell_metadata = cell_metadata,
  gene_metadata = gene_metadata
)

# ===== 4. Preprocess CDS and use Seurat's UMAP embedding =====
cds <- preprocess_cds(cds, method = 'PCA', num_dim = 50)
cds <- reduce_dimension(cds, reduction_method = "UMAP", preprocess_method = 'PCA')

# UMAP generated by Monocle3
p1 <- plot_cells(cds, reduction_method = "UMAP", color_cells_by = "seurat_clusters", show_trajectory_graph = FALSE) + 
  ggtitle("Monocle3 UMAP")

# Import UMAP coordinates from Seurat object
int.embed <- Embeddings(seurat_subset_non, reduction = "umap")
int.embed <- int.embed[rownames(cds@int_colData$reducedDims$UMAP), ]
cds@int_colData$reducedDims$UMAP <- int.embed

# UMAP using imported Seurat coordinates
p2 <- plot_cells(cds, reduction_method = "UMAP", color_cells_by = "seurat_clusters", show_trajectory_graph = FALSE) + 
  ggtitle("Seurat UMAP")

# Save side-by-side UMAP comparison
p <- p1 | p2
ggsave("2.Reduction_Compare_nonAD.pdf", plot = p, width = 10, height = 5)

# ===== 5. Cluster cells in Monocle3 =====
cds <- cluster_cells(cds, reduction_method = "UMAP")

# Visualize cell partitions
p1 <- plot_cells(cds, color_cells_by = "partition", show_trajectory_graph = FALSE) + 
  ggtitle("Partitioning")
ggsave("3.Cluster_Partition_nonAD.pdf", plot = p1, width = 6, height = 5)

# ===== 6. Learn trajectory graph =====
cds <- learn_graph(cds, learn_graph_control = list(euclidean_distance_ratio = 0.8))

# Visualize trajectory graph
p <- plot_cells(cds, color_cells_by = "partition", label_groups_by_cluster = FALSE, 
                label_leaves = FALSE, label_branch_points = FALSE)
ggsave("4.Trajectory_nonAD.pdf", plot = p, width = 6, height = 5)

# ===== 7. Order cells in pseudotime =====
cds <- order_cells(cds)

# Visualize pseudotime
p <- plot_cells(cds, color_cells_by = "pseudotime", label_cell_groups = FALSE,
                label_leaves = FALSE, label_branch_points = FALSE)
ggsave("5.Trajectory_Pseudotime_nonAD.pdf", plot = p, width = 8, height = 6)